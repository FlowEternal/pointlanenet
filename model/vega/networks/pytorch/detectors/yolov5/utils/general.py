def labels_to_class_weights(labels, nc=80):
    # Get class weights (inverse frequency) from training labels
    if labels[0] is None:  # no labels loaded
        return torch.Tensor()

    labels = np.concatenate(labels, 0)  # labels.shape = (866643, 5) for COCO
    classes = labels[:, 0].astype(np.int)  # labels = [class xywh]
    weights = np.bincount(classes, minlength=nc)  # occurrences per class #! minlength指定至少出现的数量个数，因为有可能最后的几个定义类别没有，这样bincount计算的类别个数就不对

    # Prepend gridpoint count (for uCE training)
    # gpi = ((320 / 32 * np.array([1, 2, 4])) ** 2 * 3).sum()  # gridpoints per image
    # weights = np.hstack([gpi * len(labels)  - weights.sum() * 9, weights * 9]) ** 0.5  # prepend gridpoints to start

    weights[weights == 0] = 1  # replace empty bins with 1 #! 对于那些没有的类别，将它的权重参数设置为1
    weights = 1 / weights  # number of targets per class #! 反向比例计算
    weights /= weights.sum()  # normalize #! 标准化
    return torch.from_numpy(weights)


def labels_to_image_weights(labels, nc=80, class_weights=np.ones(80)):
    # Produces image weights based on class_weights and image contents
    class_counts = np.array([np.bincount(x[:, 0].astype(np.int), minlength=nc) for x in labels]) #! 每张图内各个类别的数量
    image_weights = (class_weights.reshape(1, nc) * class_counts).sum(1) #! 根据框的相关权重去决定每张图所占的权重
    # index = random.choices(range(n), weights=image_weights, k=1)  # weight image sample
    return image_weights


